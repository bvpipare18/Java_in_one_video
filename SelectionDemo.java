public class SelectionDemo {
    public static void main(String[] args) {

        // ternary Operator
        // ?: --> condition?exp1:exp2;
        int i = 6;
        int j = 0;

        /*
         * if (i>6) { j=1; } else { j=2; }
         */
        j = i > 6 ? 1 : 2;
        System.out.println(j);

    }
}
